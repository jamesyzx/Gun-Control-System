    private void creategunBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_creategunBtnActionPerformed
        int quantity = (Integer) quantitySpinner2.getValue();

        Gun gun = (Gun) gunComboBox.getSelectedItem();
        int p=manufactorEnterprise.findgunnumber(gun, quantity);

        if(p>=0)
        {
            GunInventoryitem gunInventoryitem = manufactorEnterprise.findguninventory(gun);

            if (gunInventoryitem != null) {

                int oldquantity=gunInventoryitem.getGuninventory();
                int oldprice=gunInventoryitem.getPrice();
                int price=(quantity*p+oldprice*oldquantity)/(quantity+oldquantity);
                gunInventoryitem.setPrice(price+(Integer.valueOf(wantTF.getText())));
                
                gunInventoryitem.setSaleprice(price+(Integer.valueOf(wantTF.getText())));
                

                gunInventoryitem.setnum(quantity);

            } else {
                manufactorEnterprise.createguninventoryitem(gun, quantity,p);

            }
            populateguntable();
            populatematerialtable();
            populateproductmaterialtable();
        }
        else
        {JOptionPane.showMessageDialog(null,"Inventory Error");}
    }//GEN-LAST:event_creategunBtnActionPerformed

