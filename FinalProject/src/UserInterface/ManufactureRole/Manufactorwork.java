    private void buybTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buybTNActionPerformed
        // TODO add your handling code here:
         int quantity = (Integer) quantitySpinner1.getValue();

        Material material = (Material) materialComboBox.getSelectedItem();
        int p=Integer.valueOf(priceTF.getText());
        materialtotalprice=materialtotalprice+p*quantity;
        MaterialInventoryitem materialInventoryitem = manufactorEnterprise.findInventory(material);

        if ((materialInventoryitem != null)) {
            int oldquantity=materialInventoryitem.getQunatity();
            int oldprice=materialInventoryitem.getPrice();
            int price=(quantity*p+oldprice*oldquantity)/(quantity+oldquantity);
            materialInventoryitem.setPrice(price);
            materialInventoryitem.setnum(quantity);

        } else {
            manufactorEnterprise.creatematerialinventory(material, quantity,p);

        }

        populatematerialtable();
        populateproductmaterialtable();
        expenseTF.setText(Integer.toString(materialtotalprice));
        manufactorEnterprise.setExpense(materialtotalprice);
    }//GEN-LAST:event_buybTNActionPerformed
      public void populatematerialtable() {
        DefaultTableModel model = (DefaultTableModel) materialTable.getModel();
        model.setRowCount(0);

        for (MaterialInventoryitem material : manufactorEnterprise.getMaterialInventory()) {
            Object[] row = new Object[3];
            row[0] = material.getMaterial();
            row[1] = material.getQunatity();
            row[2] = material.getPrice();
            model.addRow(row);
        }
    }

    public void populateproductmaterialtable() {
        DefaultTableModel model = (DefaultTableModel) productmaterialTable.getModel();
        model.setRowCount(0);

        for (MaterialInventoryitem materialInventoryitem : manufactorEnterprise.getMaterialInventory()) {
            Object[] row = new Object[2];
            row[0] = materialInventoryitem.getMaterial();
            row[1] = materialInventoryitem.getQunatity();
            model.addRow(row);
        }
    }

