    private void init() {
        String[] name = person.getName().split(" ");
        txt1.setText(name[1]);
        txt2.setText(name[0]);
        txt1.setEnabled(false);
        txt2.setEnabled(false);
        txt3.setText("");
        txt4.setText("");
        txt5.setText("");
        txt6.setText("");
        txt7.setText("");
        txt8.setText("");
        if (person.isVerificationlock()) {
            txt3.setEnabled(false);
            txt4.setEnabled(false);
            txt5.setEnabled(false);
            txt6.setEnabled(false);
            txt7.setEnabled(false);
            txt8.setEnabled(false);
            rbtn1.setEnabled(false);
            rbtn2.setEnabled(false);
            checkbox.setEnabled(false);
            Save1.setEnabled(false);
            Save2.setEnabled(false);
            Apply.setEnabled(false);
        } else {
            txt3.setEnabled(true);
            txt4.setEnabled(true);
            txt5.setEnabled(true);
            txt6.setEnabled(true);
            txt7.setEnabled(false);
            txt8.setEnabled(false);
            rbtn1.setEnabled(true);
            rbtn2.setEnabled(true);
            checkbox.setEnabled(true);
        }
        request = null;
        if (userAccount.getWorkQueue().getWorkRequestList().size() > 0) {
            for (WorkRequest workRequest : userAccount.getWorkQueue().getWorkRequestList()) {
                if (workRequest instanceof VerificationRequest) {
                    request = (VerificationRequest) workRequest;
                }
            }
            if (request.getResult() != null) {
                request = new VerificationRequest();
                userAccount.getWorkQueue().getWorkRequestList().add(request);
            } else {
                if (request.getGender() != null) {
                    if (request.getGender().equals("Male")) {
                        rbtn1.setSelected(true);
                    }
                    if (request.getGender().equals("Female")) {
                        rbtn1.setSelected(false);
                        rbtn2.setSelected(true);
                    }
                }
                txt3.setText(String.valueOf(request.getAge()));
                txt4.setText(String.valueOf(request.getSsn()));
                txt5.setText(request.getPhone());
                txt6.setText(request.getHighschool());
                if (request.isUnistatus()) {
                    checkbox.setSelected(true);
                    txt7.setEnabled(true);
                    txt8.setEnabled(true);
                    txt7.setText(request.getUnivercity());
                    txt8.setText(request.getDegree());
                } else {
                    checkbox.setSelected(false);
                    txt7.setText("");
                    txt8.setText("");
                }
            }
        } else {
            request = new VerificationRequest();
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }
    }

