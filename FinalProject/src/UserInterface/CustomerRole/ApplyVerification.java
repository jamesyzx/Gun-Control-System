    private void Save1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Save1ActionPerformed
        request.setName(txt2.getText() + " " + txt1.getText());
        if (rbtn1.isSelected()) {
            request.setGender("Male");
        }
        if (rbtn2.isSelected()) {
            request.setGender("Female");
        }
        if (!txt3.getText().equals("")) {
            request.setAge(Integer.parseInt(txt3.getText()));
        }

        if (!txt4.getText().equals("")) {
            request.setSsn(Integer.parseInt(txt4.getText()));
        }
        if (!txt5.getText().equals("")) {
            request.setPhone(txt5.getText());
        }

    }//GEN-LAST:event_Save1ActionPerformed

