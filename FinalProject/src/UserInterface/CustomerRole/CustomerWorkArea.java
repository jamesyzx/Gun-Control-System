    private void btn3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn3ActionPerformed
        JPasswordField jpf = new JPasswordField(24);
        JLabel jl = new JLabel("Please input your password:     ");
        Box box = Box.createHorizontalBox();
        box.add(jl);
        box.add(jpf);
        int x = JOptionPane.showConfirmDialog(null, box, "Password Entry", JOptionPane.OK_CANCEL_OPTION);
        if (x == JOptionPane.CANCEL_OPTION) {
            return;
        }
        if (jpf.getText().equals("sEcrEt")) {
            box.removeAll();
            jl = new JLabel("Code:  ");
            jpf = new JPasswordField(24);
            box.add(jl);
            box.add(jpf);
            x = JOptionPane.showConfirmDialog(null, box, "Code Entry", JOptionPane.OK_CANCEL_OPTION);
            if (x == JOptionPane.CANCEL_OPTION) {
                return;
            }            
            boolean flag = false;
            for (Network n : system.getNetworkList()) {
                for (Enterprise enter : n.getEnterpriseDirectory().getEnterpriseList()) {
                    if (enter instanceof DistributorEnterprise) {
                        DistributorEnterprise en = (DistributorEnterprise) enter;
                        if (jpf.getText().equals(en.getCode())) {
                            BlackGun panel = new BlackGun(display, userAccount, en);
                            display.add("BlackGun", panel);
                            CardLayout layout = (CardLayout) display.getLayout();
                            layout.next(display);
                            flag = true;
                            break;
                        }
                    }
                }
                if (flag) {
                    break;
                }
            }
            return;
        }
        if (!jpf.getText().equals(userAccount.getPassword())) {
            JOptionPane.showMessageDialog(null, "Sorry, your password is incorrect!");
            return;
        }
        if (!userAccount.getPerson().isCertificatestatus()) {
            JOptionPane.showMessageDialog(null, "Please apply certificate first!");
            return;
        }

        ShopGuns panel = new ShopGuns(display, userAccount, network);
        display.add("ShopGuns", panel);
        CardLayout layout = (CardLayout) display.getLayout();
        layout.next(display);
    }//GEN-LAST:event_btn3ActionPerformed

