    public void poplabel() {
        Person person = userAccount.getPerson();
        if (!person.isVerificationlock()) {
            lbl.setText("Apply your verification first!");
        } else if (!person.isVerificationstatus()) {
            lbl.setText("Wait for pending your verification!");
        } else if (!person.isCertificatelock()) {
            lbl.setText("You are ready to apply your certificate!");
        } else if (!person.isCertificatestatus()) {
            lbl.setText("Government is pending your certificate");
        } else {
            lbl.setText("You're welcome to shop!");
        }
    }

