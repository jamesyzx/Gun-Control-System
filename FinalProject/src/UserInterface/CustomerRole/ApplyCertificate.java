    private void SendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SendActionPerformed
        Organization org = null;
        for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
            if (enterprise instanceof GovernmentEnterprise) {
                for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                    if (organization instanceof CertificateOrganization) {
                        org = organization;
                    }
                }
            }
        }
        if (org == null) {
            JOptionPane.showMessageDialog(null, "Certificate Department does not exist!");
        }
        CertificateRequest certificateRequest = new CertificateRequest();
        userAccount.getWorkQueue().getWorkRequestList().add(certificateRequest);
        certificateRequest.setStatus("Unprocessed");
        certificateRequest.setSender(userAccount);
        userAccount.getPerson().setCertificatelock(true);
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Date date = new Date();
        certificateRequest.setDate(dateFormat.format(date));
        certificateRequest.setMail(txt.getText());
        org.getWorkQueue().getWorkRequestList().add(certificateRequest);
        JOptionPane.showMessageDialog(null, "Sent succesfully!");
        Send.setEnabled(false);
        poptable(false);

    }//GEN-LAST:event_SendActionPerformed

