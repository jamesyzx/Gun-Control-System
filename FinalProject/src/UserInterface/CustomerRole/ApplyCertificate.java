    private void poptable(boolean flag) {
        DefaultTableModel dtm = (DefaultTableModel) tbl.getModel();
        dtm.setRowCount(0);
        for (WorkRequest workRequest : userAccount.getWorkQueue().getWorkRequestList()) {
            if (workRequest instanceof CertificateRequest) {
                if ((flag) && (workRequest.getReceiver() == userAccount)) {
                    Object[] row = new Object[2];
                    CertificateRequest certificateRequest = (CertificateRequest) workRequest;
                    row[0] = certificateRequest.getSender();
                    row[1] = certificateRequest;
                    dtm.addRow(row);
                }
                if ((!flag) && (workRequest.getSender() == userAccount)) {
                    Object[] row = new Object[2];
                    CertificateRequest certificateRequest = (CertificateRequest) workRequest;
                    row[0] = certificateRequest.getReceiver();
                    if (certificateRequest.getReceiver() == null) {
                        row[0] = "Request has not been issued";
                    }
                    row[1] = certificateRequest;
                    dtm.addRow(row);
                }
            }
        }
    }

